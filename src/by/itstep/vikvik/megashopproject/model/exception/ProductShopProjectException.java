package by.itstep.vikvik.megashopproject.model.exception;

public class ProductShopProjectException extends Exception {
    public ProductShopProjectException() {
        super();
    }

    public ProductShopProjectException(String message) {
        super(message);
    }

    public ProductShopProjectException(String message, Throwable cause) {
        super(message, cause);
    }

    public ProductShopProjectException(Throwable cause) {
        super(cause);
    }
}
